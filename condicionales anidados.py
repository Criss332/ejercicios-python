print("ingrese numero del 1 al 10 para convertir en numeros romanos")

numero=(int(input()))

if numero==1:
	print("el numero 1 en numero romano es: I")
	
elif numero==2:
	print("el numero 2 en numero romano es: II")

elif numero==3:
	print("el numero 3 en numero romano es: III")

elif numero==4:
	print("el numero 4 en numero romano es: IV")

elif numero==5:
	print("el numero 5 en numero romano es: V")

elif numero==6:
	print("el numero 6 en numero romano es: VI")

elif numero==7:
	print("el numero 7 en numero romano es: VII")

elif numero==8:
	print("el numero 8 en numero romano es: VIII")

elif numero==9:
	print("el numero 9 en numero romano es: IX")

